package com.kh.eeum.domain;

public class Reservation {
	private int rs_no;
	private String rs_exid;
	private String rs_uid;
	private String rs_date;
	private String rs_time;
	private String rs_money;
	private String rs_state;
	
	public int getRs_no() {
		return rs_no;
	}
	public void setRs_no(int rs_no) {
		this.rs_no = rs_no;
	}
	public String getRs_exid() {
		return rs_exid;
	}
	public void setRs_exid(String rs_exid) {
		this.rs_exid = rs_exid;
	}
	public String getRs_uid() {
		return rs_uid;
	}
	public void setRs_uid(String rs_uid) {
		this.rs_uid = rs_uid;
	}
	public String getRs_date() {
		return rs_date;
	}
	public void setRs_date(String rs_date) {
		this.rs_date = rs_date;
	}
	public String getRs_time() {
		return rs_time;
	}
	public void setRs_time(String rs_time) {
		this.rs_time = rs_time;
	}
	public String getRs_money() {
		return rs_money;
	}
	public void setRs_money(String rs_money) {
		this.rs_money = rs_money;
	}
	public String getRs_state() {
		return rs_state;
	}
	public void setRs_state(String rs_state) {
		this.rs_state = rs_state;
	}
	
}
